object Test extends App {
  println("=======foo=======")
  Macros.foo()
  Macros.foo(1)
  Macros.foo(1, 2)
  Macros.foo(1, 2, 3)
  Macros.foo(List(): _*)
  Macros.foo(List(1): _*)
  Macros.foo(List(1, 2): _*)
  Macros.foo(List(1, 2, 3): _*)
  Macros.foo(xs = List(): _*)
  Macros.foo(xs = 1)
  Macros.foo(xs = 1, 2)
  Macros.foo(xs = List(): _*)
  Macros.foo(xs = List(1): _*)
  Macros.foo(xs = List(1, 2): _*)
  Macros.foo(xs = List(1, 2, 3): _*)

  println()
  println("=======bar=======")
  Macros.bar(1)
  Macros.bar(1, 2)
  Macros.bar(1, 2, 3)
  Macros.bar(1, 2, 3, 4)
  Macros.bar(x = 1)
  Macros.bar(x = 1, 2)
  Macros.bar(x = 1, 2, 3)
  Macros.bar(x = 1, 2, 3, 4)
  Macros.bar(x = 1, xs = 2)
  Macros.bar(x = 1, xs = 2, 3)
  Macros.bar(x = 1, xs = List(): _*)
  Macros.bar(x = 1, xs = List(2): _*)
  Macros.bar(x = 1, xs = List(2, 3): _*)
  Macros.bar(x = 1, xs = List(2, 3, 4): _*)
  Macros.bar(xs = 2, x = 1)
  // doesn't compile: Macros.bar(xs = 2, 3, x = 1)
  Macros.bar(xs = List(): _*, x = 1)
  Macros.bar(xs = List(2): _*, x = 1)
  Macros.bar(xs = List(2, 3): _*, x = 1)
  Macros.bar(xs = List(2, 3, 4): _*, x = 1)

  println()
  println("=======baz=======")
  Macros.baz(1, 2)
  Macros.baz(1, 2, 3)
  Macros.baz(1, 2, 3, 4)
  Macros.baz(1, 2, 3, 4, 5)
  Macros.baz(x = 1, y = 2)
  Macros.baz(x = 1, y = 2, 3)
  Macros.baz(x = 1, y = 2, 3, 4)
  Macros.baz(x = 1, y = 2, 3, 4, 5)
  Macros.baz(x = 1, y = 2)
  Macros.baz(x = 1, y = 2, xs = 3)
  Macros.baz(x = 1, y = 2, xs = 3, 4)
  Macros.baz(x = 1, y = 2, xs = 3, 4, 5)
  Macros.baz(x = 1, y = 2, xs = List(): _*)
  Macros.baz(x = 1, y = 2, xs = List(3): _*)
  Macros.baz(x = 1, y = 2, xs = List(3, 4): _*)
  Macros.baz(x = 1, y = 2, xs = List(3, 4, 5): _*)
  // doesn't compile: Macros.baz(y = 2, x = 1)
  // doesn't compile: Macros.baz(y = 2, x = 1, 3)
  // doesn't compile: Macros.baz(y = 2, x = 1, 3, 4)
  // doesn't compile: Macros.baz(y = 2, x = 1, 3, 4, 5)
  Macros.baz(xs = 3, x = 1, y = 2)
  // doesn't compile: Macros.baz(xs = 3, 4, x = 1, y = 2)
  // doesn't compile: Macros.baz(xs = 3, 4, 5, x = 1, y = 2)
  Macros.baz(xs = List(): _*, x = 1, y = 2)
  Macros.baz(xs = List(3): _*, x = 1, y = 2)
  Macros.baz(xs = List(3, 4): _*, x = 1, y = 2)
  Macros.baz(xs = List(3, 4, 5): _*, x = 1, y = 2)
  Macros.baz(xs = 3, y = 1, x = 2)
  // doesn't compile: Macros.baz(xs = 3, 4, y = 1, x = 2)
  // doesn't compile: Macros.baz(xs = 3, 4, 5, y = 1, x = 2)
  Macros.baz(xs = List(): _*, y = 1, x = 2)
  Macros.baz(xs = List(3): _*, y = 1, x = 2)
  Macros.baz(xs = List(3, 4): _*, y = 1, x = 2)
  Macros.baz(xs = List(3, 4, 5): _*, y = 1, x = 2)
}